public class DataTab3 {
    String hargaString;
    int harga;

    public DataTab3(String hargaString, int harga){
        this.hargaString = hargaString;
        this.harga = harga;
    }

    public String getHargaString() {
        return hargaString;
    }

    public void setHargaString(String hargaString) {
        this.hargaString = hargaString;
    }

    public int getHarga() {
        return harga;
    }

    public void setHarga(int harga) {
        this.harga = harga;
    }
}
